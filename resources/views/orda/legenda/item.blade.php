                            <p>У каждого слова своя история. Не напрасно и гора Манырак получила такое имя &ndash; &laquo;Блеяние овцы&raquo;</p>
                            <p>Кочевал когда-то в этих местах на тучных пастбищах большой казахский род. Все случается в жизни: и радость, и горе, и хорошее, и плохое, но однажды казахов постигла такая беда, хуже которой не бывает. Нагрянули в степь враждебные племена, стали избивать мирных кочевников, разорять аулы, угонять стада. Собрались аксакалы на совет, задумались: где найти спасение? Одна дорога &ndash; в горы. Сложили юрты, навьючили верблюдов, собрали табуны и отары, двинулись в путь.</p>
                            <p>Крут был подъем. По дороге выбилась из сил какая-то овца, стала отставать и падать на колени. Загудели голоса:</p>

                            <p>Знатный был бай. Послушались его. Да тем себя и погубили.</p>
                            <p>Поднялась кочевка на перевал. Расположили беглецы в надежном укрытии скот и с повеселевшей душой начали готовиться к ужину и ночлегу.<br />Той порой у подножия горы проносились на конях вражеские отряды, выискивая наживу. Уже проскакали было мимо, да вдруг слышат: где-то на склоне жалобно блеет овца.</p>
                            <p>Откуда бы взяться овце в таком пустынном месте? Привстали на стременах, прислушались: снова и снова донеслось с горы блеяние, полное тоски и тревоги.<br />Повернули разбойники коней и помчались на голос животного. Вскоре увидели они брошенную овцу и караванный след, идущий к перевалу. С диким воем ворвались насильники в убежище беглецов, и потекла потоком невинная кровь по каменистым уступам. Никого из рода не осталось в живых.</p>
                            <p>Много лет прошло с того времени. Давно забылись имена погибших людей. И хранится память о страшном событии только в названии горы Манырак &ndash; &laquo;Блеяние овцы&raquo;.</p>
